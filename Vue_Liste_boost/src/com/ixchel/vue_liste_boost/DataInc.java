package com.ixchel.vue_liste_boost;



public class DataInc {
	
		private char N;
		private float Vi, Il, Pi, Vo, Io;

		public DataInc(char n, float vi, float il, float pi, float vo, float io) {
			super();
			N = n;
			Vi = vi;
			Il = il;
			Pi = pi;
			Vo = vo;
			Io = io;
		}
		public char getN() {
			return N;
		}
		public void setN(char n) {
			N = n;
		}
		public float getVi() {
			return Vi;
		}
		public void setVi(float vi) {
			Vi = vi;
		}
		public float getIl() {
			return Il;
		}
		public void setIl(float il) {
			Il = il;
		}
		public float getPi() {
			return Pi;
		}
		public void setPi(float pi) {
			Pi = pi;
		}
		public float getVo() {
			return Vo;
		}
		public void setVo(float vo) {
			Vo = vo;
		}
		public float getIo() {
			return Io;
		}
		public void setIo(float io) {
			Io = io;
		}

		
}

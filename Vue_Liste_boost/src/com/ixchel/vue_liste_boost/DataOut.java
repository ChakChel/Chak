package com.ixchel.vue_liste_boost;


public class DataOut {
	
	private char N, V;

	public DataOut(char n, char v) {
		super();
		N = n;
		V = v;
	}

	public char getN() {
		return N;
	}

	public void setN(char n) {
		N = n;
	}

	public char getV() {
		return V;
	}

	public void setV(char v) {
		V = v;
	}

}
